// Pixel-art icons drawn via CSS box-shadow — real pixel art, not SVG slop.
// Each icon is an N×N grid where '1' = pixel on, '0' = pixel off.

function PixelIcon({ grid, size = 96, color = '#fff', glow = null, style = {} }) {
  const rows = grid.trim().split('\n').map(r => r.trim());
  const n = rows.length;
  const px = size / n;
  const shadows = [];
  for (let y = 0; y < n; y++) {
    for (let x = 0; x < rows[y].length; x++) {
      const ch = rows[y][x];
      if (ch === '1' || ch === '#') {
        shadows.push(`${x * px}px ${y * px}px 0 0 ${color}`);
      } else if (ch === '2') {
        // secondary tint — 60% color
        shadows.push(`${x * px}px ${y * px}px 0 0 ${color}99`);
      } else if (ch === '3') {
        // tertiary — 30%
        shadows.push(`${x * px}px ${y * px}px 0 0 ${color}55`);
      }
    }
  }
  return (
    <div style={{ width: size, height: size, position: 'relative', ...style }}>
      <div style={{
        width: px, height: px, position: 'absolute', left: 0, top: 0,
        boxShadow: shadows.join(', '),
        filter: glow ? `drop-shadow(0 0 ${size/8}px ${glow})` : undefined,
      }} />
    </div>
  );
}

